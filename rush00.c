#include <unistd.h>

void	rush_00(int x, int y)
{
	int	i;
	int	j;

	i = 1;
	while (i <= x)
	{
		j = 1;
		while (j <= y)
		{
			if ((i == 1 || i == x) && (j == 1 || j == y))
				write (1, "o", 1);
			else if ((i == 1 || i == x) && (j > 1 && j < y))
				write (1, "-", 1);
			else if ((j == 1 || j == x) && (i > 1 && i < y))
				write (1, "|", 1);
			else
				write (1, " ", 1);
		j++;
		}
		write (1, "\n", 1);
	i++;
	}
}

int	main(void)
{
	rush_00 (5, 5);
}
